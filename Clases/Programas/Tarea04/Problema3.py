def CentigradosFahrenheit (c):
    f= ((9*c)/5) + 32
    return("%d grados centígrados equivalen a %d grados fahrenheit" % (c, f))

def FahrenheitCentigrados (x):
    y= (5*(x-32))/9
    return ("%d grados fahrenheit equivalen a %d grados centígrados" % (x, y))



    
            
